    const supportedLanguages = ["de", "en"];

    function getInitialLanguage() {
        const params = new URLSearchParams(window.location.search);
        const urlLanguage = params.get("lang");

        if (supportedLanguages.includes(urlLanguage)) {
            localStorage.setItem("wa-language", urlLanguage);
            return urlLanguage;
        }

        const savedLanguage = localStorage.getItem("wa-language");
        return supportedLanguages.includes(savedLanguage) ? savedLanguage : "de";
    }

    function translate(key, replacements) {
        const language = document.documentElement.lang || "de";
        let value = translations[language]?.[key] || translations.de[key] || key;

        if (replacements) {
            Object.keys(replacements).forEach((name) => {
                value = value.replaceAll("{" + name + "}", replacements[name]);
            });
        }

        return value;
    }

    function applyTranslations(language) {
        document.documentElement.lang = language;

        document.querySelectorAll("[data-i18n]").forEach((element) => {
            element.textContent = translate(element.dataset.i18n);
        });

        document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
            element.setAttribute("placeholder", translate(element.dataset.i18nPlaceholder));
        });

        document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
            element.setAttribute("aria-label", translate(element.dataset.i18nAria));
        });

        document.querySelectorAll("[data-i18n-content]").forEach((element) => {
            element.setAttribute("content", translate(element.dataset.i18nContent));
        });

        const titleElement = document.querySelector("[data-i18n-title]");
        if (titleElement?.dataset.i18nTitle) {
            document.title = translate(titleElement.dataset.i18nTitle);
        }

        document.querySelectorAll("[data-lang-switch]").forEach((button) => {
            const isActive = button.dataset.langSwitch === language;
            button.classList.toggle("is-active", isActive);
            button.setAttribute("aria-pressed", String(isActive));
        });

        window.dispatchEvent(new CustomEvent("wa:languagechange", { detail: { language } }));
    }

    function setLanguage(language) {
        if (!supportedLanguages.includes(language)) {
            return;
        }

        localStorage.setItem("wa-language", language);
        applyTranslations(language);
    }

    function applyTheme(theme) {
        const normalizedTheme = theme === "light" ? "light" : "dark";
        document.documentElement.dataset.theme = normalizedTheme;
        document.documentElement.style.colorScheme = normalizedTheme;
        localStorage.setItem("wa-theme", normalizedTheme);

        const toggle = document.querySelector("[data-theme-toggle]");
        if (toggle) {
            const nextThemeKey = normalizedTheme === "dark" ? "nav.themeToLight" : "nav.themeToDark";
            toggle.setAttribute("aria-label", translate(nextThemeKey));
        }

        const metaTheme = document.querySelector('meta[name="theme-color"]');
        if (metaTheme) {
            metaTheme.setAttribute("content", normalizedTheme === "dark" ? "#0a0a0a" : "#f7f5ef");
        }
    }

    function initTheme() {
        const savedTheme = localStorage.getItem("wa-theme");
        const preferredTheme = window.matchMedia("(prefers-color-scheme: light)").matches ? "light" : "dark";
        applyTheme(savedTheme || preferredTheme);

        document.querySelector("[data-theme-toggle]")?.addEventListener("click", () => {
            const currentTheme = document.documentElement.dataset.theme === "light" ? "light" : "dark";
            applyTheme(currentTheme === "light" ? "dark" : "light");
        });
    }

    function initLanguage() {
        setLanguage(getInitialLanguage());

        document.querySelectorAll("[data-lang-switch]").forEach((button) => {
            button.addEventListener("click", () => {
                setLanguage(button.dataset.langSwitch);
                applyTheme(document.documentElement.dataset.theme);
            });
        });
    }

    window.WebAgenturI18n = {
        t: translate,
        setLanguage,
        get language() {
            return document.documentElement.lang || "de";
        }
    };

    initLanguage();
    initTheme();
